input_text = input("Please enter the cabin class: ")
if input_text == "LUX":
    print("upper-deck cabin with a balcony.")
elif input_text == "A":
    print("above the car deck, equipped with a window.")
elif input_text == "B":
    print("windowless cabin above the car deck.")
elif input_text == "C":
    print("windowless cabin above the car deck.")
else:
    print("Invalid cabin class.")